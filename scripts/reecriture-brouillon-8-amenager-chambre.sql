## Délimiter des zones

Même dans une seule pièce, séparez visuellement et mentalement :
- **Zone travail** : bureau près d'une fenêtre (lumière du jour), perpendiculaire à celle-ci pour éviter les reflets sur l'écran.
- **Zone repos** : le lit, distinct du bureau. Une plante, un paravent ou un simple changement d'éclairage suffit à marquer la frontière — ça aide le cerveau à décrocher du travail.
- **Zone détente** : un fauteuil et une liseuse, orientés autrement que le lit.

## Soigner la lumière

La lumière pilote votre énergie et votre concentration.
- Maximisez le jour : rideaux légers, et un miroir bien placé agrandit et éclaire la pièce.
- Multipliez les sources le soir : une lampe de bureau à variateur (blanc froid ~5000-6500K pour travailler), une liseuse chaude (~2700K) pour se détendre.

## Optimiser le rangement

Dans un petit espace, l'ordre libère de la place… et de la charge mentale.
- Jouez la **verticalité** : étagères murales, patères, boîtes sous le lit.
- « Tout a sa place » : un emplacement fixe par objet, qu'on y remet systématiquement.
- Faites tourner la garde-robe selon les saisons.

## Quelques plantes

Le Sansevieria ou le Pothos sont quasi increvables et assainissent l'air ; la lavande favorise le sommeil. Choisissez selon l'exposition de la chambre et votre rythme (espèces résistantes si vous voyagez souvent).

## Un coin télétravail correct

Les jours où vous travaillez depuis la maison (jusqu'à 2 par semaine pour un frontalier — voir notre [guide du télétravail](/blog/teletravail-frontalier-geneve-regles-2026)) :
- **Ergonomie** : écran à hauteur des yeux, support + clavier/souris externes si vous êtes sur un portable, un bon fauteuil.
