## L'alternative maligne : école française + anglais à côté

Toutes les familles n'ont pas 28 000 CHF/an/enfant à mettre dans l'écolage. L'alternative qui monte chez les frontaliers : **scolariser en école publique française (gratuite) ou privée sous contrat (modérée), et ajouter de l'anglais en parascolaire intensif** (camps, cours, séjours, nounou anglophone).

**Quand ça marche :** enfant jeune (maternelle/primaire), parents au moins partiellement anglophones, objectif bilinguisme « fonctionnel » plus que diplôme international. Le Pays de Gex et Annemasse ont d'excellentes écoles publiques, avec beaucoup d'enfants d'expatriés.

**Quand ça marche moins :** ados visant une université anglophone (l'IB ou les A-Levels font la différence sur dossier), ou famille très mobile qui rechange de pays (la continuité d'un cursus international compte). Là, l'école internationale garde son avantage.

## Questions fréquentes

**Quel est le prix moyen d'une école internationale à Genève ?**
Compte **27 000 à 37 000 CHF/an et par enfant** en primaire-secondaire dans les grandes écoles (Écolint, Collège du Léman, GES). Florimont est plus accessible (dès ~19 000 CHF). Ajoute inscription, capital fee à l'entrée, cantine et bus.

**Mon enfant peut-il aller à l'école publique suisse en habitant en France ?**
En principe **non** : l'école publique genevoise est réservée aux résidents du canton. Les frontaliers se tournent donc soit vers le **privé international à Genève**, soit vers l'**école française** côté domicile. Quelques rares dérogations existent mais ne constituent pas une stratégie fiable.

**Quelle école prend le moins de temps de trajet depuis Annemasse ?**
Côté rive gauche/sud : **Écolint La Grande Boissière (Champel)** et **Institut Florimont (Petit-Lancy)** sont les plus logiques, accessibles via le **Léman Express** ou en voiture hors heures de pointe. Les campus rive droite (Nations, Versoix) imposent de traverser Genève — à éviter au quotidien depuis Annemasse.

**Y a-t-il des bus scolaires depuis la France ?**
Oui pour les grandes écoles, surtout côté **Pays de Gex**. Les arrêts et lignes varient chaque année — confirme la desserte de ta commune **avant** de signer un bail. Côté Annemasse, le Léman Express est souvent l'option la plus fiable pour les ados autonomes.

**À partir de quel âge inscrire en école internationale ?**
La plupart accueillent dès **3 ans** (Pre-Reception / maternelle). Plus tôt l'enfant entre, plus l'immersion linguistique est naturelle — mais les places en primaire sont disputées, alors inscris-toi **12 à 18 mois à l'avance** pour les écoles populaires.

