## Le vrai défi frontalier : le double trajet école + boulot

Le piège classique : choisir une école sans regarder où on va habiter, puis découvrir qu'on enchaîne **deux bouchons frontaliers par matin** — un pour déposer les enfants, un pour rejoindre son bureau de l'autre côté du lac.

### La règle d'or : aligne le campus, ton bureau et ta commune
- Tu travailles **dans le quartier des Nations / OI** (ONU, OMS, CICR) ? Vise un campus **rive droite** (Nations, Collège du Léman) et habite **Ferney-Voltaire ou Saint-Genis-Pouilly**.
- Tu travailles **au centre / Champel / Plainpalais / Lancy** ? Vise **La Grande Boissière ou Florimont** (rive gauche) et habite **Annemasse / Ville-la-Grand**, avec le Léman Express en renfort.

Cet alignement t'évite de traverser tout Genève deux fois par jour. C'est le facteur n°1 de qualité de vie pour une famille frontalière — bien plus que les 2 000 CHF d'écolage en plus ou en moins.

### Les bus scolaires côté France
La plupart des grandes écoles (Écolint, Collège du Léman, GES) opèrent des **lignes de bus qui passent côté français** (Pays de Gex notamment, parfois Annemasse selon l'année). C'est un supplément (souvent 3 000–5 000 CHF/an) mais ça change la vie : l'enfant est pris en charge, tu files au travail. Vérifie les arrêts desservis **avant** de signer un bail — ils varient chaque année selon la demande.

### Le carpooling parents
Là où le bus ne passe pas, les familles s'organisent en **roulement de covoiturage** entre parents de la même école et de la même commune. Les groupes WhatsApp d'école sont la vraie infrastructure ici. À anticiper dès l'inscription.

