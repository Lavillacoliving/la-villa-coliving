## Quels frais peut-on légalement vous facturer en colocation ?

Commençons par les faits, sans jargon. Quand une **agence immobilière** met un logement en location, la loi — le cadre dit « ALUR » — l'autorise à partager une partie de ses honoraires avec le locataire, mais **uniquement pour quatre prestations bien précises** :

1. l'organisation de la visite du logement ;
2. la constitution de votre dossier de location ;
3. la rédaction du bail ;
4. la réalisation de l'**état des lieux d'entrée**.

C'est tout. Ces quatre prestations — et aucune autre — peuvent vous être facturées lorsqu'une agence intervient.

Deuxième point important : ces honoraires ne sont pas libres. Ils sont **plafonnés au mètre carré**, selon un barème fixé par la loi (avec des plafonds plus stricts en « zone tendue », ce qui est le cas de toute l'agglomération genevoise côté France). Concrètement, le montant **varie selon la surface** du logement : plus la chambre ou l'appartement est petit, plus le plafond est bas.

Enfin, sachez que **certains frais réclamés ici ou là ne sont tout simplement pas autorisés**. L'**état des lieux de sortie**, par exemple, ne peut pas vous être facturé — seul celui d'entrée entre dans le cadre légal. Idem pour d'éventuels « frais de renouvellement » de bail. Si on vous présente ce type de ligne, vous êtes en droit de demander sur quelle base elle repose.

Rien de tout cela n'est illégal en soi : une agence qui facture ces quatre prestations, dans la limite des plafonds, est dans son droit. L'idée ici n'est pas de pointer du doigt, mais de vous donner les clés pour lire une offre — et comprendre ce que vous payez.

